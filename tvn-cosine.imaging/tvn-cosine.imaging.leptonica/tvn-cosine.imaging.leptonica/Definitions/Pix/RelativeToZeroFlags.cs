namespace Leptonica
{
    /// <summary>
    /// Relative to zero flags
    /// </summary>
    public enum RelativeToZeroFlags
    {
        /// <summary>
        /// Choose values less than zero  
        /// </summary>
        L_LESS_THAN_ZERO = 1,
        /// <summary>
        /// Choose values equal to zero   
        /// </summary>
        L_EQUAL_TO_ZERO = 2,
        /// <summary>
        /// Choose values greater than zero  
        /// </summary>
        L_GREATER_THAN_ZERO = 3
    }
}
