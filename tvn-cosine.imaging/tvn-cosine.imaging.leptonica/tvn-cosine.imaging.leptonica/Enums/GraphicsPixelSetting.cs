namespace Leptonica
{
    /// <summary>
    /// Graphics pixel setting
    /// </summary>
    public enum GraphicsPixelSetting
    {
        /// <summary>
        /// set all bits in each pixel to 1    
        /// </summary>
        L_SET_PIXELS = 1,
        /// <summary>
        /// set all bits in each pixel to 0  
        /// </summary>
        L_CLEAR_PIXELS = 2,
        /// <summary>
        /// flip all bits in each pixel  
        /// </summary>
        L_FLIP_PIXELS = 3    
    }
}
