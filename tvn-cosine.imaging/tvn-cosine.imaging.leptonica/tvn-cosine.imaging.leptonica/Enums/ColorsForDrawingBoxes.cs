        /// </summary>
        L_DRAW_GREEN = 1,
        /// <summary>
        /// draw in blue   
        /// </summary>
        L_DRAW_BLUE = 2,
        /// <summary>
        /// draw specified color 
        /// </summary>
        L_DRAW_SPECIFIED = 3,
        /// <summary>
        /// draw as sequence of r,g,b 
        /// </summary>
        L_DRAW_RGB = 4,
        /// <summary>
        /// draw randomly chosen colors
        /// </summary>
        L_DRAW_RANDOM = 5      
    }
}
