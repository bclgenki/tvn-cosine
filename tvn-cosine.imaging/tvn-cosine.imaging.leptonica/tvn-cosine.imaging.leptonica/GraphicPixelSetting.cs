namespace leptonica.net
{
    public enum GraphicPixelSetting
    {
        /// <summary>
        /// set all bits in each pixel to 1 
        /// </summary>
        SET_PIXELS = 1,
        /// <summary>
        /// set all bits in each pixel to 0    
        /// </summary>
        CLEAR_PIXELS = 2,
        /// <summary>
        ///  flip all bits in each pixel    
        /// </summary>
        FLIP_PIXELS = 3
    }
}
